package Basics;

        /*
        
        Java is a **high-level, object-oriented programming language** that is widely used for building applications, websites, mobile apps, and enterprise software.

        It was originally developed by James Gosling and released by [Oracle Corporation](https://www.oracle.com/java/?utm_source=chatgpt.com) (originally created at Sun Microsystems).

        ### Key Features of Java

        * **Platform Independent**: Java programs are compiled into **bytecode**, which can run on any device that has a **Java Virtual Machine (JVM)**. This is often summarized as:

        > "Write Once, Run Anywhere" (WORA)

        * **Object-Oriented**: Uses classes and objects to organize code.

        * **Secure**: Includes built-in security features such as bytecode verification and sandboxing.

        * **Robust**: Strong memory management and exception handling help reduce errors.

        * **Multithreaded**: Can perform multiple tasks simultaneously.

        ### Simple Java Example

        ```java
        public class HelloWorld {
            public static void main(String[] args) {
                System.out.println("Hello, World!");
            }
        }
        ```

        **Output:**

        ```
        Hello, World!
        ```

        ### Common Uses of Java

        * Android app development
        * Web applications
        * Enterprise software
        * Cloud-based services
        * Banking and financial systems
        * Big data technologies (e.g., Apache Hadoop)

        ### How Java Works

        1. Write Java source code (`.java` file).
        2. Compile it using the Java compiler (`javac`).
        3. The compiler produces **bytecode** (`.class` file).
        4. The **JVM** executes the bytecode on the target machine.

        ### Java vs. JavaScript

        Despite the similar names, **Java and JavaScript are different languages**:

        * **Java**: General-purpose, compiled to bytecode, strongly typed.
        * **JavaScript**: Primarily used for web pages, interpreted/JIT-compiled, dynamically typed.

      
        */

